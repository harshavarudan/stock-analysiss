def printAll(percentage, stock):
    df = get_current_price(stock)
    global text
    arr = df['Open'].tolist()
    try:
        curr = get_current_price_now(stock)
    except:
        return
    percentage_change = 1 - curr / highest(arr)
    if percentage_change >= percentage:
        text += (stock + "value: " + str(curr) + " change:" + str(percentage_change) + "\n\n")


